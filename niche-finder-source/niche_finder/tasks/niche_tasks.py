class NicheFinderTasks:
    """利基产品发现器任务集合"""
    
    # ==================== 蓝海模式专属任务 ====================
    
    def blue_ocean_discovery_task(self, agent, domain: str, constraints: dict = None) -> Task:
        """蓝海模式 - 发现空白市场机会"""
        
        constraints_desc = ""
        if constraints:
            constraints_str = ", ".join([f"{k}:{v}" for k, v in constraints.items()])
            constraints_desc = f"约束条件：{constraints_str}"
        
        description = dedent(f"""
        在【{domain}】领域中寻找蓝海市场机会。
        {constraints_desc}
        
        你的目标是找到3-5个低竞争、高增长、被大公司忽略的利基机会。
        
        请按以下步骤工作：
        1. 先用Web Search了解{domain}领域的整体市场格局和主要玩家
        2. 搜索用户抱怨、差评、痛点集中的地方（论坛、Reddit、知乎、产品评论等）
        3. 寻找"需求在增长但供给不足"的细分领域
        4. 思考新技术（AI、自动化、新平台）能否让以前不可行的方案变得可行
        
        请严格按照以下格式输出3-5个具体的蓝海机会：
        
        # 🔍 蓝海机会发现报告
        
        **分析领域**：{domain}
        **发现数量**：3-5个优质蓝海机会
        
        ---
        
        ## 机会 1：[机会名称]
        
        🎯 **一句话总结**：[用一句话说清楚这个机会是什么]
        
        👥 **目标用户**：
        - 具体是谁：[描述越具体越好]
        - 用户规模：[估算大概有多少这样的用户]
        - 付费意愿：[强/中/弱，为什么]
        
        💢 **核心痛点**：
        1. [痛点1 - 具体描述，最好有真实例子]
        2. [痛点2 - 具体描述]
        3. [痛点3 - 具体描述]
        
        🚀 **为什么是蓝海**：
        - 竞争情况：[现有玩家有哪些，为什么他们没做好]
        - 增长趋势：[有什么数据/趋势表明需求在增长]
        - 被忽略原因：[为什么大公司没进入这个领域]
        
        💡 **产品雏形**：
        - 核心功能：[MVP只需要哪1-2个核心功能]
        - 技术方案：[大概用什么技术实现，难度如何]
        - 差异化：[和现有方案比，最大的不同是什么]
        
        💰 **商业模式**：
        - 收费方式：[订阅/按次/免费增值等]
        - 预期定价：[大概多少钱，依据是什么]
        - 盈利周期：[大概多久能回本]
        
        ⚠️ **主要风险**：[最大的1-2个风险]
        
        ---
        
        ## 机会 2：[机会名称]
        [同样的结构]
        
        ---
        
        ## 机会 3：[机会名称]
        [同样的结构]
        
        ---
        
        ## 机会 4-5：[如果有的话]
        
        ---
        
        ## 📋 总结对比表
        
        | 机会 | 市场大小 | 竞争烈度 | 技术难度 | 盈利速度 | 综合推荐 |
        |------|---------|---------|---------|---------|---------|
        | 机会1 | 大/中/小 | 低/中/高 | 低/中/高 | 快/中/慢 | ⭐⭐⭐⭐⭐ |
        | 机会2 | ... | ... | ... | ... | ... |
        
        **一句话建议**：[最值得先验证的是哪个，为什么]
        """)
        
        expected_output = "3-5个蓝海机会的详细列表，每个包含用户、痛点、竞争分析和产品雏形"
        
        return Task(
            description=description,
            expected_output=expected_output,
            agent=agent,
            async_execution=False,
        )
    
    # ==================== 红海细分模式专属任务 ====================
    
    def red_ocean_niche_discovery_task(self, agent, big_market: str, angle: str = "未被满足的需求") -> Task:
        """红海细分模式 - 在大市场中找细分机会"""
        
        description = dedent(f"""
        在【{big_market}】这个已经很拥挤的红海中，寻找{angle}的利基机会。
        
        你的目标是从主流产品忽略的缝隙中，找到3-5个值得做的细分方向。
        
        请按以下步骤工作：
        1. 先搜索{big_market}的主要玩家有哪些，他们的产品定位和定价
        2. 深入研究用户对这些产品的负面评价和抱怨（App Store、G2、知乎、小红书等）
        3. 寻找"用户在用奇怪的组合方案解决问题"的场景
        4. 识别有特殊需求但被主流产品忽视的小众用户群
        
        分析维度：
        - 功能维度：有没有某一个功能用户特别需要但主流产品做得很烂？
        - 用户维度：有没有某一类用户的需求没有被专门满足？
        - 场景维度：有没有某个特定使用场景没有被很好覆盖？
        - 价格维度：有没有对价格特别敏感的用户没有便宜的选择？
        
        请输出3-5个具体的细分机会，每个机会包含：
        - 🎯 细分机会名称
        - 👥 这个细分的目标用户是谁（越具体越好）
        - 💢 他们的核心痛点是什么，为什么主流产品满足不了
        - 📊 这个细分市场大概有多大（有多少人/多少付费能力）
        - ⚔️ 为什么大玩家不做这个细分（市场太小/利润太薄/不符合战略）
        - 💡 我们的差异化切入点是什么
        """)
        
        expected_output = "3-5个红海细分机会的详细分析，每个包含用户画像、痛点、市场规模和切入点"
        
        return Task(
            description=description,
            expected_output=expected_output,
            agent=agent,
            async_execution=False,
        )
    
    # ==================== 跨界机会模式专属任务 ====================
    
    def cross_domain_discovery_task(self, agent, domain_a: str, domain_b: str) -> Task:
        """跨界模式 - 寻找两个领域交叉的创新机会"""
        
        description = dedent(f"""
        探索【{domain_a}】和【{domain_b}】两个领域交叉融合的创新机会。
        
        你的目标是找到3-5个有商业价值的跨界产品方向。
        
        思考维度：
        1. 技术跨界：A领域的成熟技术能否解决B领域的老问题？
        2. 模式跨界：A领域验证过的商业模式能否复制到B领域？
        3. 用户跨界：有没有同时属于两个领域的用户群，他们的需求没有被满足？
        4. 数据跨界：两个领域的数据结合能否产生新的价值？
        5. 体验跨界：A领域的用户体验标准能否降维打击B领域？
        
        请按以下步骤工作：
        1. 先分别了解{domain_a}和{domain_b}领域的最新趋势和核心痛点
        2. 思考两个领域的交集点在哪里
        3. 搜索有没有人已经在做类似的跨界尝试，效果如何
        4. 评估每个机会的可行性和商业价值
        
        请输出3-5个具体的跨界产品机会，每个包含：
        - 🎯 产品概念（一句话说清楚是什么）
        - 🔀 跨界逻辑（A和B怎么结合的）
        - 👥 目标用户是谁
        - 💢 解决了什么具体问题
        - 📈 为什么现在是好时机（趋势/技术/认知成熟了）
        - 💰 初步商业可行性评估
        - ⚠️ 最大的风险是什么
        """)
        
        expected_output = "3-5个跨界创新机会的详细分析，每个包含产品概念、跨界逻辑、用户和商业模式"
        
        return Task(
            description=description,
            expected_output=expected_output,
            agent=agent,
            async_execution=False,
        )
    
    # ==================== 通用：单个机会深度评估任务 ====================
    
    def opportunity_evaluation_task(self, agent, opportunity: str, opportunity_name: str) -> Task:
        """对单个机会进行多维度深度评估"""
        
        description = dedent(f"""
        对以下创业机会进行全面的多维度评估：
        
        【机会名称】{opportunity_name}
        【机会描述】{opportunity}
        
        请从以下维度进行评估：
        
        1. **市场维度**
           - 市场规模有多大？是存量还是增量市场？
           - 增长趋势如何？
           - 用户付费意愿强不强？
           - 有没有政策或监管风险？
        
        2. **竞争维度**
           - 直接竞争对手有哪些？他们做得怎么样？
           - 间接竞争对手和替代品有哪些？
           - 我们的差异化优势是什么？
           - 进入壁垒高不高？会不会很快被抄？
        
        3. **商业维度**
           - 推荐什么商业模式？定价多少？
           - 获取一个用户大概要花多少钱？
           - 单位经济模型能跑通吗？
           - 天花板有多高？
        
        4. **技术维度**
           - MVP需要哪些核心功能？
           - 技术难度大不大？有没有硬骨头？
           - 有没有现成的开源方案或API可以用？
           - 一个全栈工程师多久能做出来？
        
        5. **综合评分**（每项1-10分）
           - 市场吸引力
           - 竞争烈度（分越高越容易进）
           - 商业可行性
           - 技术可行性
           - 时机成熟度
           - 加权总分
        
        请尽量使用搜索获取真实数据来支撑你的判断。
        """)
        
        expected_output = "单个机会的全面评估报告，包含市场、竞争、商业、技术四维分析和综合评分"
        
        return Task(
            description=description,
            expected_output=expected_output,
            agent=agent,
            async_execution=False,
        )
    
